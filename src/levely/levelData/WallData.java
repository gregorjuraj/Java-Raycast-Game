package levely.levelData;

public class WallData {
    private int horizontalnaStenaTextura;
    private int vertikalnaStenaTextura;
    private int x;
    private int y;

    public int getHorizontalnaStenaTextura() {
        return this.horizontalnaStenaTextura;
    }

    public void setHorizontalnaStenaTextura(int horizontalnaStenaTextura) {
        this.horizontalnaStenaTextura = horizontalnaStenaTextura;
    }

    public int getVertikalnaStenaTextura() {
        return this.vertikalnaStenaTextura;
    }

    public void setVertikalnaStenaTextura(int vertikalnaStenaTextura) {
        this.vertikalnaStenaTextura = vertikalnaStenaTextura;
    }

    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }
}


